// פונקציית עזר לקריאה ל-Gemini API
async function callGemini(prompt, systemInstruction, responseSchema) {
  const models = [
    "gemini-1.5-flash",
    "gemini-1.5-flash-8b",
    "gemini-1.0-pro",
    "gemini-pro",
  ];

  if (!apiKey || apiKey.trim() === "") {
    throw new Error("מפתח ה-API חסר. אנא הכנס מפתח תקין בשורה של apiKey בקוד.");
  }

  let lastError;

  for (let i = 0; i < models.length; i++) {
    const modelName = models[i];
    const url =
      "https://generativelanguage.googleapis.com/v1beta/models/" +
      modelName +
      ":generateContent?key=" +
      apiKey;

    const payload = {
      contents: [{ parts: [{ text: prompt }] }],
    };

    if (systemInstruction) {
      payload.systemInstruction = { parts: [{ text: systemInstruction }] };
    }

    if (responseSchema) {
      payload.generationConfig = {
        responseMimeType: "application/json",
        responseSchema: responseSchema,
      };
    }

    try {
      const response = await fetch(url, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });

      if (!response.ok) {
        const errorData = await response.text();
        throw new Error(
          `API Error ${response.status} (${modelName}): ${errorData}`
        );
      }

      const data = await response.json();

      let textResponse = "";
      if (
        data &&
        data.candidates &&
        data.candidates[0] &&
        data.candidates[0].content &&
        data.candidates[0].content.parts &&
        data.candidates[0].content.parts[0]
      ) {
        textResponse = data.candidates[0].content.parts[0].text;
      }

      if (!textResponse) {
        throw new Error("גוגל לא החזירה טקסט.");
      }

      try {
        if (responseSchema) {
          const cleanJson = textResponse
            .replace(/```json/g, "")
            .replace(/```/g, "")
            .trim();
          return JSON.parse(cleanJson);
        }
        return textResponse;
      } catch (parseError) {
        if (responseSchema) return null;
        return textResponse;
      }
    } catch (error) {
      lastError = error;
      console.warn(`Model ${modelName} failed, trying next...`);
      if (error.message.includes("API_KEY_INVALID")) {
        throw new Error("מפתח ה-API שהוזן אינו תקין. ודא שהעתקת אותו במלואו.");
      }
    }
  }

  throw lastError;
}

