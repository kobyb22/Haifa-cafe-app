// קומפוננטת עמוד הפירוט (Modal)
function CafeModal({ cafe, onClose }) {
  const [suggestion, setSuggestion] = useState(null);
  const [isSuggesting, setIsSuggesting] = useState(false);
  const [aiErrorMsg, setAiErrorMsg] = useState("");

  useEffect(() => {
    document.body.style.overflow = "hidden";
    return () => {
      document.body.style.overflow = "unset";
    };
  }, []);

  const handleGetSuggestion = async () => {
    setIsSuggesting(true);
    setAiErrorMsg("");
    const promptText =
      "You are a local foodie in Haifa. I am visiting this cafe:\nName: " +
      cafe.name +
      "\nDescription: " +
      cafe.fullDescription +
      "\nTags: " +
      cafe.tags.join(", ") +
      "\n\nWhat is one highly specific, mouth-watering order (a drink and maybe food/pastry) you recommend I get here? Respond in one short, enthusiastic sentence in Hebrew.";
    try {
      const response = await callGemini(
        promptText,
        "Be creative, sound like a cool local recommending a secret menu item or the absolute best thing on the menu.",
        null
      );
      setSuggestion(response);
    } catch (e) {
      setAiErrorMsg(e.message || "שגיאה כללית");
      setSuggestion(
        "מצטערים, לא הצלחנו לקבל המלצה כרגע. אנא ודא שמפתח ה-API הוגדר בקוד וששם המודל תקין."
      );
    } finally {
      setIsSuggesting(false);
    }
  };

  return (
    <div
      className="fixed inset-0 z-50 flex items-center justify-center p-4 sm:p-6 bg-stone-900/60 backdrop-blur-sm"
      onClick={onClose}
    >
      <div
        className="bg-[#fcfaf8] rounded-3xl w-full max-w-3xl max-h-[90vh] overflow-y-auto shadow-2xl relative flex flex-col"
        onClick={(e) => e.stopPropagation()}
        dir="rtl"
      >
        <button
          onClick={onClose}
          className="absolute top-4 left-4 bg-white/80 p-2.5 rounded-full hover:bg-white text-stone-700 shadow-sm z-10 transition-colors"
        >
          <X size={20} strokeWidth={2.5} />
        </button>

        <div className="h-64 sm:h-80 w-full relative bg-stone-200 shrink-0">
          <img
            src={cafe.image}
            alt={cafe.name}
            onError={(e) => {
              e.target.onerror = null;
              e.target.src =
                "https://images.unsplash.com/photo-1497935586351-b67a49e012bf?auto=format&fit=crop&q=80&w=800";
            }}
            className="w-full h-full object-cover"
          />
        </div>

        <div className="p-6 sm:p-8 flex flex-col gap-6">
          <div className="flex flex-col sm:flex-row sm:items-start justify-between gap-4 border-b border-[#f0ebe1] pb-6">
            <div>
              <h2 className="text-3xl font-black text-amber-900 mb-2">
                {cafe.name}
              </h2>
              <div className="flex items-center gap-2 bg-amber-100/50 w-fit px-3 py-1 rounded-lg text-sm font-bold text-amber-900 border border-amber-200/50">
                <Star
                  size={16}
                  fill="currentColor"
                  className="text-amber-500"
                />
                {cafe.rating}{" "}
                <span className="text-amber-700/60 font-normal">
                  ({cafe.reviews} ביקורות מגוגל)
                </span>
              </div>
            </div>

            <a
              href={cafe.mapsLink}
              target="_blank"
              rel="noopener noreferrer"
              className="bg-amber-700 hover:bg-amber-800 text-white px-5 py-2.5 rounded-xl flex items-center justify-center gap-2 font-medium transition-colors shrink-0"
            >
              <Navigation size={18} /> פתח בגוגל מפות
            </a>
          </div>

          <div className="flex flex-wrap gap-2">
            {cafe.tags.map((tag) => {
              const config = tagConfig[tag];
              if (!config) return null;
              return (
                <div
                  key={tag}
                  className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-sm font-medium border ${config.activeClass}`}
                >
                  <span>{config.emoji}</span> {tag}
                </div>
              );
            })}
          </div>

          <div>
            <h3 className="text-lg font-bold text-stone-900 mb-2">על המקום</h3>
            <p className="text-stone-600 leading-relaxed text-base">
              {cafe.fullDescription}
            </p>
          </div>

          <div className="bg-white rounded-2xl p-5 border border-[#f0ebe1] grid grid-cols-1 sm:grid-cols-2 gap-4">
            <div className="flex items-start gap-3">
              <MapPin className="text-amber-600 mt-0.5 shrink-0" size={20} />
              <div>
                <p className="font-bold text-stone-800 text-sm">כתובת</p>
                <p className="text-stone-600 text-sm mt-0.5">{cafe.address}</p>
              </div>
            </div>
            <div className="flex items-start gap-3">
              <Clock className="text-amber-600 mt-0.5 shrink-0" size={20} />
              <div>
                <p className="font-bold text-stone-800 text-sm">שעות פתיחה</p>
                <p className="text-stone-600 text-sm mt-0.5">{cafe.hours}</p>
              </div>
            </div>
            <div className="flex items-start gap-3">
              <Phone className="text-amber-600 mt-0.5 shrink-0" size={20} />
              <div>
                <p className="font-bold text-stone-800 text-sm">טלפון</p>
                <p className="text-stone-600 text-sm mt-0.5" dir="ltr">
                  {cafe.phone}
                </p>
              </div>
            </div>
          </div>

          {/* AI פיצ'ר בעמוד הפירוט */}
          <div className="bg-amber-50 rounded-2xl p-5 border border-amber-100 mt-2">
            <div className="flex items-center gap-2 mb-3">
              <Sparkles className="text-amber-600" size={20} />
              <h3 className="font-bold text-amber-900">ההמלצה של ה-AI</h3>
            </div>

            {!suggestion && !isSuggesting && (
              <button
                onClick={handleGetSuggestion}
                className="bg-white border border-amber-200 text-amber-800 hover:bg-amber-100 font-medium px-4 py-2 rounded-lg flex items-center gap-2 transition-colors text-sm"
              >
                <MessageCircleHeart size={16} /> מה כדאי לי להזמין כאן?
              </button>
            )}

            {isSuggesting && (
              <div className="text-sm text-amber-700 flex items-center gap-2 font-medium">
                <Loader2 size={16} className="animate-spin" /> מכין לך המלצה
                מושלמת...
              </div>
            )}

            {suggestion && !isSuggesting && (
              <div className="text-base text-stone-800 font-medium leading-relaxed">
                <p>"{suggestion}"</p>
                {aiErrorMsg && (
                  <p className="text-xs text-red-500 mt-2">
                    שגיאת מערכת: {aiErrorMsg}
                  </p>
                )}
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}
